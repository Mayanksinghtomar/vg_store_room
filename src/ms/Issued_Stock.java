    public Issued_Stock() {
        initComponents();
      DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        // Clear the existing table
        int rows = model.getRowCount();
        if (rows > 0) {
            for (int i = 0; i < rows; i++) {
                model.removeRow(0);
            }
        }
        try {
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            com.mysql.jdbc.Connection con = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/Store_house", "root", "123");
            com.mysql.jdbc.Statement stmt = (com.mysql.jdbc.Statement) con.createStatement();
            ResultSet rs = stmt.executeQuery("Select * From Issued_Items ORDER BY Date DESC");
            // Iterate through the result and display on screen
            while (rs.next()) {
                String name = rs.getString("Item_Name");
                String size = rs.getString("Size");
                String quantity = rs.getString("Quantity");
                String issuedto = rs.getString("Issued_To");
                String issuedby = rs.getString("Issued_By");
                String date = rs.getString("Date");
                System.out.println(name + "|" + size + "|" + quantity + "|" + issuedto + "|" + issuedby + "|" + date);
                model.addRow(new Object[]{name, size, quantity, issuedto, issuedby, date});
            }
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }

